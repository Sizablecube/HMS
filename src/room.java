    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd", Locale.ENGLISH);
        Date xin = cin.getDate();
        Date xout = cout.getDate();

        long diffsec = Math.abs(xout.getTime() - xin.getTime());
        long diff = TimeUnit.DAYS.convert(diffsec, TimeUnit.MILLISECONDS);
        if(box.getSelectedIndex()==0){
            long x = 120*diff;
            price.setText("R"+String.valueOf(x));
        }
        else{
            long x = 100*diff;
            price.setText("R"+String.valueOf(x));
        }

        day.setText(String.valueOf(diff));
    }//GEN-LAST:event_jButton5ActionPerformed

