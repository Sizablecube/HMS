    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try{
            Connection conn=DriverManager.getConnection("jdbc:mysql://localhost/hms","root","");

            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String date = sdf.format(cin.getDate());
            String Date = sdf.format(cout.getDate());
            String query = "UPDATE `booking` SET `Room type`='"+String.valueOf(box.getSelectedItem())+"',`Check out`='"+date+"',`Check in`='"+Date+"' WHERE  `booking`.`ID` ='"+ID.getText()+"';";
            PreparedStatement pst =conn.prepareStatement(query);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Update success");

        }
        catch(SQLException e){
            System.err.print(e);
        }

    }//GEN-LAST:event_jButton3ActionPerformed

